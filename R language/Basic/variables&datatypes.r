# Variables and Data Types
integer_var <- 42       # Integer
double_var <- 3.14      # Double (Float)
char_var <- "Hello R!"  # Character String
logical_var <- TRUE     # Logical (Boolean)

# Print variable types
print(paste("Integer Variable:", integer_var))
print(paste("Double Variable:", double_var))
print(paste("Character Variable:", char_var))
print(paste("Logical Variable:", logical_var))
